#include "../include/xystd.h"
#include <math.h>

double xy_sqrt(double x) {
    return sqrt(x);
}

double xy_pow(double base, double exp) {
    return pow(base, exp);
}

double xy_sin(double x) {
    return sin(x);
}

double xy_cos(double x) {
    return cos(x);
}

double xy_abs_f64(double x) {
    return fabs(x);
}

int xy_abs_i32(int x) {
    return abs(x);
}

